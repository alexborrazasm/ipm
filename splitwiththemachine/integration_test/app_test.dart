import 'dart:math';

import 'package:flutter/material.dart';
import 'package:flutter_test/flutter_test.dart';
import 'package:font_awesome_flutter/font_awesome_flutter.dart';
import 'package:intl/intl.dart';
import 'package:splitwiththemachine/main.dart';
import 'package:integration_test/integration_test.dart';
import 'package:mockito/mockito.dart';
import 'package:mockito/annotations.dart';

import 'package:splitwiththemachine/data/repositories.dart';
import 'package:splitwiththemachine/data/services.dart';
import 'package:splitwiththemachine/data/models.dart';

import 'mock_setup.dart';

// Only way to generate mocks with null safety!
@GenerateMocks(<Type>[SplitWithMeService])
import 'app_test.mocks.dart'; // File generated by build_runner

void main() {
  // Force English (US) locale before all tests
  Intl.defaultLocale = 'en_US';

  IntegrationTestWidgetsFlutterBinding.ensureInitialized();

  final mockService = buildMockService();

  group('end-to-end test', () {
    testWidgets('load the initial expense data', (
        tester,
        ) async {
      // Load app widget.
      await tester.pumpWidget(MyApp(
        expenseRepository: ExpenseRepository(service: mockService),
        friendRepository: FriendRepository(service: mockService),
      ));

      await tester.pumpAndSettle();

      expect(find.byIcon(FontAwesomeIcons.creditCard), findsNWidgets(6));
      expect(find.text("Groceries"), findsOneWidget);
      expect(find.text("Coffee"), findsOneWidget);
      expect(find.text("Bus Ticket"), findsOneWidget);
      expect(find.text("Dinner"), findsOneWidget);
      expect(find.text("Books"), findsOneWidget);
      expect(find.text("Gym Membership"), findsOneWidget);
      expect(find.text("Balance: \$ 0.00"), findsExactly(5));
      expect(find.text("Balance: \$ 3.30"), findsOne);
    });

    testWidgets('load details of a expense (no friends added)', (
        tester,
        ) async {
      // Load app widget.
      await tester.pumpWidget(MyApp(
        expenseRepository: ExpenseRepository(service: mockService),
        friendRepository: FriendRepository(service: mockService),
      ));

      await tester.pumpAndSettle();

      final expenseRow = find.byKey(ValueKey("expense-2"));
      final inkWell = find.descendant(
          of: expenseRow, matching: find.byType(InkWell)
      ).first;
      await tester.tap(inkWell);

      await tester.pumpAndSettle();

      // Expense detail
      expect(find.text("Coffee"), findsExactly(2));
      expect(find.text("\$ 3.10"), findsOne);
      expect(find.text("\$ 0.00"), findsOne);
      expect(find.text("Saturday, January 4, 2025"), findsOne);

      // Friends on expense, nothing
      expect(find.text("No friends added"), findsOne);
    });

    testWidgets('load details of a expense (with friends added)', (
        tester,
        ) async {
      // Load app widget.
      await tester.pumpWidget(MyApp(
        expenseRepository: ExpenseRepository(service: mockService),
        friendRepository: FriendRepository(service: mockService),
      ));

      await tester.pumpAndSettle();

      final expenseRow = find.byKey(ValueKey("expense-5"));
      final inkWell = find.descendant(
          of: expenseRow, matching: find.byType(InkWell)
      ).first;
      await tester.tap(inkWell);

      await tester.pumpAndSettle();

      // Expense detail
      expect(find.text("Books"), findsExactly(2));
      expect(find.text("\$ 120.60"), findsOne);
      expect(find.text("\$ 3.30"), findsOne);
      expect(find.text("Saturday, March 2, 2024"), findsOne);

      // Friends on expense
      expect(find.byType(CircleAvatar), findsExactly(3));
      expect(find.text("Nerea"), findsOne);
      expect(find.textContaining("Credit: \$ 0.00"), findsOneWidget);
      expect(find.text("Alex"), findsOne);
      expect(find.textContaining("Credit: \$ 1.30"), findsOne);
      expect(find.text("Dani"), findsOne);
      expect(find.textContaining("Credit: \$ 2.00"), findsOne);
      expect(find.textContaining("Debit: \$ 40.20"), findsExactly(3));
    });

    testWidgets('add an expense', (
        tester,
        ) async {
      // Load app widget.
      await tester.pumpWidget(MyApp(
        expenseRepository: ExpenseRepository(service: mockService),
        friendRepository: FriendRepository(service: mockService),
      ));

      await tester.pumpAndSettle();
      await tester.pumpAndSettle();

    });

    testWidgets('add friend to expense (expense no friends)', (
        tester,
        ) async {
      // Load app widget.
      await tester.pumpWidget(MyApp(
        expenseRepository: ExpenseRepository(service: mockService),
        friendRepository: FriendRepository(service: mockService),
      ));

      await tester.pumpAndSettle();

      final expenseRow = find.byKey(ValueKey("expense-2"));
      final inkWellExpense = find.descendant(
          of: expenseRow, matching: find.byType(InkWell)
      ).first;
      await tester.tap(inkWellExpense);

      await tester.pumpAndSettle();

      final addFriendFab = find.byTooltip("Add friend");
      await tester.tap(addFriendFab);
      await tester.pumpAndSettle();

      expect(find.byType(CircleAvatar), findsExactly(6));
      expect(find.text("Nerea"), findsOneWidget);
      expect(find.text("Pepe"), findsOneWidget);
      expect(find.text("Dani"), findsOneWidget);
      expect(find.text("Alex"), findsOneWidget);
      expect(find.text("Marcos"), findsOneWidget);
      expect(find.text("Manolo"), findsOneWidget);

      final friendRow = find.byKey(ValueKey("friend-5"));
      final inkWellFriend = find.descendant(
          of: friendRow, matching: find.byType(InkWell)
      ).first;
      await tester.tap(inkWellFriend);
      await tester.pumpAndSettle();

      expect(find.text("'Marcos' added to 'Coffee'"), findsOne);
      expect(find.text("Marcos"), findsOne);
      expect(find.textContaining("Credit: \$ 0.00"), findsOne);
      expect(find.textContaining("Debit: \$ 1.55"), findsOne);
    });

  });
}